package com.example.demo;

public class Fruit {
            private long no;
            private String name;

    public Fruit(long no, String name) {
        this.no = no;
        this.name = name;
    }

    public long getNo() {
        return no;
    }

    public void setNo(long no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
